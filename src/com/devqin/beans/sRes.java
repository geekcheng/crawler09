package com.devqin.beans;

//搜索结果对象，标题title，摘要desc，链接link，抓取时间ctime
public class sRes {

	public String title;
	public String desc;
	public String link;
	public String ctime;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getCtime() {
		return ctime;
	}
	public void setCtime(String c_time) {
		this.ctime = c_time;
	}
	
	

}



